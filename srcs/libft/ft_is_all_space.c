#include "libft.h"

int	ft_is_all_space(char *str)
{
	printf("ft_is_all_space: %s\n", str);
	while ((*str >= '\t' && *str <= '\r') || *str == ' ' || *str == '\n')
		str ++;
	if (*str == '\0')
		return (1);
	return (0);
}
